
public class Arrays {

	public static void main(String[] args) {
		// define the size
		int[] array = new int[6];
		array[0] = 155;
		array[5] = 77;
		System.out.println(array[5]);
		
		// array initializer
		String[] words = {"abc", "def", "ghi", "jkl"};
		words[0] = "dee";
		System.out.println(words[0]);
		
	}
}
